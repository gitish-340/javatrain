package Operator;

public class IncrementDecrement {

	public static void main(String[] args) {
		int a=40,b=50;
		System.out.println("value of a "+a);//40
		System.out.println("value of b "+b);//50
		System.out.println("value of --a " +(--a));//39
		System.out.println("value of a  "+a);//39
		System.out.println("value of b-- "+(b--));//50
		System.out.println("value of b"+b);//49

	}

}
